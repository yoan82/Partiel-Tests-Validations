package com.example.testetvalisation

class Calcul {

    fun calculAdd(a:Int, b:Int):Int{
        return (a+b)
    }


    fun calculSub(a:Int, b:Int):Int{
        return (a-b)
    }


    fun calculDiv(a:Int, b:Int):Int{
        return (a/b)
    }


    fun calculMultiple(a:Int, b:Int):Int{
        return (a*b)
    }
}